/*

You'll sometimes see numbers involved in arithmetic referred
to as operands.

An operand is the part of an instruction representing the data
manipulated by the operator. For example, when you add two numbers,
the numbers are the operand and "+" is the operator.

And what to be performed between the two operands is the operation.

*/

// Example of some operators

let x = 5;

// Operator +=
console.log((x += 4));

// Operator -=
console.log((x -= 3));

// Operator *=
console.log((x *= 3));

// Operator /=
console.log((x /= 6));


/*

You'll sometimes see numbers involved in arithmetic referred
to as operands.

An operand is the part of an instruction representing the data
manipulated by the operator. For example, when you add two numbers,
the numbers are the operand and "+" is the operator.

And what to be performed between the two operands is the operation.

In another word,
An operand – is what operators are applied to. For instance,
in the multiplication of 5 * 2 there are two operands: the left
operand is 5 and the right operand is 2. Sometimes, people call
these “arguments” instead of “operands”.

An operator is unary if it has a single operand. For example,
the unary negation - reverses the sign of a number.
let x = 1;
x = -x;

An operator is binary if it has two operands.

*/

// Example of some operators

let x = 5;

// Operator +=
console.log((x += 4));

// Operator -=
console.log((x -= 3));

// Operator *=
console.log((x *= 3));

// Operator /=
console.log((x /= 6));


/* eslint-disable no-plusplus */
/* You can only increment an existing variable */

let number = 4;

// console.log(number);
// console.log(++number);
console.log(number);

// Returns the result immediately
console.log(++number);

// Does not returns the result immediately
console.log(number++);

console.log(number);

/* Increment/decrement can only be applied to variables. */
